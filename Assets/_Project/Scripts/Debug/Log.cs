using UnityEngine;
using TowerConquest.Debug;

namespace TowerConquest.Debug
{
    public static class Log
    {
        public static void Info(object message)
        {
            Log.Info(message);
        }

        public static void Warning(object message)
        {
            Log.Warning(message);
        }

        public static void Error(object message)
        {
            Log.Error(message);
        }
    }
}
